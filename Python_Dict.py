# DataType Dictionary

a = {1: "Shubham", 2: "Kondekar", "Age": 44}

print(a[1])  # Printing First value from Dictionary

print(a)  # Printing Dictionary

a["Middle Name"] = "ABC"
print(a)  # Inserting new element in Dictionary

