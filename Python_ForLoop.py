# For Loop Program
# This below for loop is printing list elements
obj = [1, 2, 3, 4, 5, 6]

for i in obj:
    print(i)
# This below for loop is printing 1 to 10 numbers
for a in range(1, 11):
    print(a)

# This below for loop is used for summing all the natural numbers
print("sum values")
summation = 0
for b in range(1, 6):
    summation = summation + b
    print(summation)
